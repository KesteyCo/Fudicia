// True infinite partner carousel: loops forever, drag left or right, resumes immediately after hover.
(() => {
  const tracks = document.querySelectorAll('.partner-track');

  tracks.forEach((track) => {
    let offset = 0;
    let setWidth = 0;
    let isHovering = false;
    let isDragging = false;
    let lastPointerX = 0;
    let lastTime = performance.now();
    const speed = 34; // pixels per second; low = premium slow movement

    const measure = () => {
      // The carousel contains two identical logo sets, so one loop is half the full track.
      setWidth = track.scrollWidth / 2;
    };

    const normalize = () => {
      if (!setWidth) return;
      offset = ((offset % setWidth) + setWidth) % setWidth;
    };

    const render = () => {
      normalize();
      track.style.transform = `translate3d(${-offset}px, 0, 0)`;
    };

    const tick = (now) => {
      const delta = Math.min((now - lastTime) / 1000, 0.05);
      lastTime = now;

      if (!isHovering && !isDragging) {
        offset += speed * delta;
        render();
      }

      requestAnimationFrame(tick);
    };

    measure();
    render();
    requestAnimationFrame(tick);

    window.addEventListener('resize', () => {
      measure();
      render();
    }, { passive: true });

    track.addEventListener('mouseenter', () => {
      isHovering = true;
    });

    track.addEventListener('mouseleave', () => {
      isHovering = false;
      isDragging = false;
      track.classList.remove('is-dragging');
      // The next animation frame resumes the endless loop immediately.
    });

    track.addEventListener('pointerdown', (event) => {
      isDragging = true;
      isHovering = true;
      lastPointerX = event.clientX;
      track.classList.add('is-dragging');
      track.setPointerCapture(event.pointerId);
    });

    track.addEventListener('pointermove', (event) => {
      if (!isDragging) return;

      const dx = event.clientX - lastPointerX;
      lastPointerX = event.clientX;

      // Dragging right decreases offset; dragging left increases offset.
      offset -= dx;
      render();
    });

    const endDrag = (event) => {
      if (!isDragging) return;
      isDragging = false;
      track.classList.remove('is-dragging');
      try { track.releasePointerCapture(event.pointerId); } catch (_) {}
    };

    track.addEventListener('pointerup', endDrag);
    track.addEventListener('pointercancel', endDrag);
  });
})();


// Reset navbar logo animation timer whenever dark/light mode changes.
// This keeps the full-logo/minilogo sequence synchronized after switching themes.
(() => {
  const restartNavbarLogoCycle = () => {
    const animatedParts = document.querySelectorAll('.nav-simple-full, .nav-simple-mini');

    animatedParts.forEach((part) => {
      part.style.animation = 'none';
    });

    // Force a reflow so the browser truly restarts the keyframes.
    void document.body.offsetHeight;

    requestAnimationFrame(() => {
      animatedParts.forEach((part) => {
        part.style.animation = '';
      });
    });
  };

  let lastThemeState = document.body.classList.contains('theme-light');

  const observer = new MutationObserver(() => {
    const currentThemeState = document.body.classList.contains('theme-light');

    if (currentThemeState !== lastThemeState) {
      lastThemeState = currentThemeState;
      restartNavbarLogoCycle();
    }
  });

  observer.observe(document.body, {
    attributes: true,
    attributeFilter: ['class']
  });

  document.querySelectorAll('[data-theme-toggle], .theme-toggle, .floating-theme-toggle, #themeToggle, #theme-toggle').forEach((button) => {
    button.addEventListener('click', () => {
      setTimeout(restartNavbarLogoCycle, 0);
    });
  });
})();


// HARD navbar logo timer reset.
// Resets the animation clock to frame 0 whenever theme or language controls are clicked.
// Result: full logo visible again, then 15s countdown starts fresh.
(() => {
  const navBrand = () => document.querySelector('.brand-simple-cycle');
  let resetToken = 0;

  const resetNavbarLogoClock = () => {
    const brand = navBrand();
    if (!brand) return;

    const full = brand.querySelector('.nav-simple-full');
    const minis = brand.querySelectorAll('.nav-simple-mini');
    const token = ++resetToken;

    brand.classList.add('nav-cycle-resetting');

    if (full) {
      full.style.setProperty('animation', 'none', 'important');
      full.style.setProperty('opacity', '1', 'important');
    }

    minis.forEach((mini) => {
      mini.style.setProperty('animation', 'none', 'important');
      mini.style.setProperty('opacity', '0', 'important');
      mini.style.setProperty('transform', 'translateY(-50%) scale(.94) rotate(0deg)', 'important');
    });

    // Force browser to flush the stopped animation state.
    void brand.offsetHeight;

    window.setTimeout(() => {
      if (token !== resetToken) return;

      brand.classList.remove('nav-cycle-resetting');

      if (full) {
        full.style.removeProperty('animation');
        full.style.removeProperty('opacity');
      }

      minis.forEach((mini) => {
        mini.style.removeProperty('animation');
        mini.style.removeProperty('opacity');
        mini.style.removeProperty('transform');
      });

      // Force frame 0 of the restarted animation to paint immediately.
      void brand.offsetHeight;
    }, 90);
  };

  const isThemeOrLanguageControl = (target) => {
    if (!(target instanceof Element)) return false;

    return Boolean(target.closest(`
      [data-theme-toggle],
      [data-lang],
      [data-language],
      .theme-toggle,
      .floating-theme-toggle,
      .mode-toggle,
      .light-toggle,
      .language-button,
      .language-option,
      .lang-option,
      .language-menu,
      .lang-menu,
      #themeToggle,
      #theme-toggle,
      #languageToggle,
      #language-toggle
    `));
  };

  // Reset immediately when the user clicks a theme or language UI element.
  document.addEventListener('click', (event) => {
    if (!isThemeOrLanguageControl(event.target)) return;

    resetNavbarLogoClock();

    // Run again just after the site code changes the theme/language,
    // so light/dark SVG swapping cannot interrupt the sequence.
    window.setTimeout(resetNavbarLogoClock, 0);
    window.setTimeout(resetNavbarLogoClock, 80);
  }, true);

  // Also reset if the site changes theme class by any method.
  let previousBodyClass = document.body.className;
  let previousHtmlClass = document.documentElement.className;

  const classObserver = new MutationObserver(() => {
    const bodyClass = document.body.className;
    const htmlClass = document.documentElement.className;

    if (bodyClass !== previousBodyClass || htmlClass !== previousHtmlClass) {
      previousBodyClass = bodyClass;
      previousHtmlClass = htmlClass;
      resetNavbarLogoClock();
    }
  });

  classObserver.observe(document.body, { attributes: true, attributeFilter: ['class'] });
  classObserver.observe(document.documentElement, { attributes: true, attributeFilter: ['class'] });
})();
